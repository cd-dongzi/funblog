import { BytemdPlugin } from 'bytemd';
export function insertMore(): BytemdPlugin {
  return {
    actions: [
      {
        title: '插入 more 标记',
        icon, // 16x16 SVG icon
        handler: {
          type: 'action',
          click(ctx) {
            const pos = ctx.appendBlock('<!-- more -->\n');
            ctx.editor.setSelection(ctx.codemirror.Pos(pos.line, 0), ctx.codemirror.Pos(pos.line, 13));
            ctx.editor.focus();
          },
        },
      },
    ],
  };
}
